package application;

public class GlobalSettings {
    private static int[] DIMENSIONS;

//********************************************************************************************************************//
//******************************************************GETTERS*******************************************************//
//********************************************************************************************************************//
    public static int[] getDimensions() { return DIMENSIONS; }
    public static int getWidth() { return DIMENSIONS[0]; }
    public static int getHeight() { return DIMENSIONS[1]; }


//********************************************************************************************************************//
//******************************************************SETTERS*******************************************************//
//********************************************************************************************************************//
    public static void setDimensions(int[] dimensions) { DIMENSIONS = dimensions; }
}
